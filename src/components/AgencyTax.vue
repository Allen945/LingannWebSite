            <div class="article" v-if="id == 7">
                <h3>企业所得税核定征收与查帐征收区别。</h3>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;1.核定征收是税务所按照一定的比例，根据销售额来征收企业所得税的。所以如果公司的年利润是负数，那么采取“核定征收”也要缴纳所得税。查帐征收就是按照净利润来算的。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;2.从做账的角度，核定征收比较简单，查账征收要复杂得多。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;1、企业所得税实行“核定应税所得率”方式征收，这主要是针对能够正确核算收入而不能正确核算成本费用的企业适用，如果能够正确核算成本费用的企业，都采用查帐征收。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;2、查帐征收与核定应税所得率征收，关键是看你的利润率与核定应税所得率谁高，如果是利润率高，实行核定应税所得率征收就会少缴税，否则就会多缴税。
                    比如：你经营收入100万元，成本费用50万元，流转环节税金10万元，利润率为40%，假如核定应税所得率为25%：查帐征收的企业所得税=（收入-成本费用-流转环节税金）*税率=（100-40-10）*33%=13.2万元；核定应税所得率征收的企业所得税=收入*核定应税所得率*税率=100*25%*33%=8.25万元；两者相差4.95万元，主要就是取决于你的利润率40%高于核定应税所得率25%的原因。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;3、如果你们财务核算制度不键全，不能正确核算成本费用的，税务部门肯定是要实行核定应税所得率方式征收。如果你们的利润率低于核定应税所得率的话，建议按规定做好财务会计核算工作，争取查帐征收（可以少缴税），否则实行核定应税所得率方式征收对你们有利。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（补充：采用核定应税所得率征收后，就是按收入直接计算，不考虑是否亏损问题。如果你们感觉承担不了，可以重新提出申请，要求实行查帐征收，不过税务局会上门核实后再确认。）</p>
