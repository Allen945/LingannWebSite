                <h3>公司注册设立时分公司和子公司的区别</h3>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;子公司与分公司是现代大公司企业经营组织的重要形式。一家公司为什么安排它的某些附属单位作为子公司，而另一些附属单位又作为分公司?子公司与分公司，到底哪个才是公司在设立分支机构的上上之选呢?在回答这些问题之前，我们先来看看分公司和子公司的特点：</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;(一)分公司
                    分公司是与总公司或本公司相对应的一个概念。许多大型企业的业务分布于全国各地甚至许多国家，直接从事这些业务的是公司所设置的分支机构或附属机构，这些分支机构或附属机构就是所谓的分公司。而公司本身则称之为总公司或本公司。
                    分公司与总公司的关系虽然同子公司与母公司的关系有些类似。但分公司的法律地位与子公司完全不同，它没有独立的法律地位。
                    分公司是总公司下属的直接从事业务经营活动的分支机构或附属机构。虽然分公司有公司字样，但它不是真正意义上的公司。因为分公司不具有企业法人资格，不具有独立的法律地位，不独立承担民事责任。
                    分公司的特征具体表现为：
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;①分公司没有自己的独立财产，其实际占有、使用的财产是总公司财产的一部分，列入总公司的资产负债表中。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;②分公司不独立承担民事责任。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;③分公司不是公司，它的设立不须依照公司设立程序，只要在履行简单地登记和营业手续后即可成立。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;④分公司没有自己的章程，没有董事会等形式的公司经营决策和业务执行机关。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;⑤分公司名称，只要在总公司名称后加上分公司字样即可。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;(二)子公司
                    子公司是与母公司相对应的法律概念。母公司是指拥有另一公司一定比例以上的股份或通过协议方式能够对另一公司实行实际控制的公司。子公司是指一定比例以上的股份被另一公司所拥有或通过协议方式受到另一公司实际控制的公司。子公司具有法人资格，可以独立承担民事责任，这是子公司与分公司的重要区别。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;1、子公司受母公司的实际控制。所谓实际控制是指母公司对子公司的一切重大事项拥有实际上的决定权，其中尤为重要的是能够决定子公司董事会的组成。在未经他人同意的情况下，母公司自己就可以通过行使权力而任命董事会的多名董事。某些信托机构虽然拥有公司的大量股份，但并不参与对公司事务的实际控制，因而不属于母公司。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;2、母公司与子公司之间的控制关系是基于股权的占有或控制协议。根据股东会多数表决原则，拥有股份越多，越能够取得对公司事务的决定权。因此，一个公司如果拥有了另一公司50%以上的股份，就必然能够对该公司实行控制。但实际上由于股份的分散，只要拥有一定比例以上的股份，就能够获股东会表决权的多数，即可取得控制的地位。除股份控制方式之外，通过订立某些特殊契约或协议而使某一公司处于另一公司的支配之下，也可以形成母公司、子公司的关系。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;3、母公司、子公司各为独立的法人。虽然子公司处于受母公司实际控制的地位，许多方面都要受到母公司的管理，有的甚至类似母公司的分支机构，但法律上，子公司仍是具有法人地位的独立公司企业，它有自己的公司名称和公司章程，并以自己的名义进行经营活动，其财产与母公司的财产彼此独立，各有自己的资产负债表。在财产责任上，子公司和母公司也各以自己所有财产为限承担各自的财产责任，互不连带。
                    通过持有其他公司一定比例以上的股份而对其实行控制的公司，又称控股公司。母公司与控股公司是可以通用的两个概念，子公司也可以通过控制其他公司一定比例以上的股份而成为控股公司，被控股的公司成为孙公司。母公司通过控制众多的子公司、孙公司而成为庞大的公司集团。母公司只要通过较少的资本就可以利用子公司的资本购买别的公司，组建起金字塔型的公司集团模式。
                </p>
