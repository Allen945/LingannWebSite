                <h3>注册公司应该如何选择经营范围</h3>
                <p>注册公司的时候经营范围具体是怎么确定，每个初创业者都考虑的事项，因为关系到创立公司的生存方向，一般情况怎么选择公司的经营范围比较合理呢?以下是很诚信为大家介绍的内容供大家参考!</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;公司的经营范围如何确定：</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;首先创立公司时应当明确公司的经营范围，公司的经营范围指的是公司在经营活动中所涉及到的领域范围，也就是公司具有的生产项目、经营种类以及服务事项等。另外公司确定经营范围需要考虑以下情况：</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;1、投资者需了解公司资金的投向，也就是投资者的资金投入的项目和需承担风险的界限;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;2、公司在经营中权利能力、行为能力的大体界定;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;3、公司董事、监事、经理可认知自己权限所及的领域;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;4、建立和维护一定的管理秩序、经营秩序。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;这个经营范围不是被有关部门确定的，而是公司自主决定经营范围必须符合规定具有合法性的，法律不许经营的项目不得私自经营，法律、行政法规规定有限制的经营项目应当为依法经过相关部门批准。</p>
