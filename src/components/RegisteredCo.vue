                <h3>上海公司注册应该避免哪些问题</h3>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;创始人拉上一帮“志同道合”的伙伴们，“大张旗鼓”地成立团队要开干后，关于公司注册的事宜就要提上日程了，注册一家上海公司，似乎很简单，找个代理服务公司开搞，不就好了嘛!但是如果你不加重视，那么你给自己挖坑的路程就从此开始。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;一、我想啥时候注册就啥时候注册</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;很多人觉的，什么时候公司注册都行，所以就等万事俱备后挑个黄道吉日再来注册公司。其实只要团队稳定后，公司越早注册越好!因为公司是一个法律主体，你要用它做很多事。比如微信公众号，您需要早点抢注保护，公众号的所有人是不能转让的，所以你不能用个人的形式认证后再转让给公司。另外，你还要招人，像拉勾这样的平台，没有营业执照是无法申请账号的。此外，如果您要做新三板或者IPO，对于公司成立年限是一项硬性要求，新三板一定要求公司成立2年后才能上。因此时间点方面，公司越早注册越好。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;二、公司类型的选择
                    公司类型包括行业(经营特点)和组织形式，以下分别说明：
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;(1)、在互联网+如此盛行的今天，你的行业形式最好和互联网沾点边，尽量用互联科技、信息科技、网络科技、电子商务、文化创意等作为行业特点，这样比你的商务咨询、贸易、工贸高大上的多，才会吸引更多客户。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;(2)、组织形式大家基本都是选择的是有限公司，但是建议除非万不得已，千万别注册一人有限公司。一人有限公司虽然股东比较简单、省事，但是一旦你选择了一人有限公司，每一会计年度终了时编制财务会计报报告，并经会计师事务所审计，这一条就会增加你很多工作量。当然如果你的公司要全资投资成立全资子公司，一人有限公司是很好的组织形式。</p>
