                    银行开户是为企业开立一个基本账户，企业可以作为一个独立的法人拥有自己的银行账户，进行网银转账等等业务处理。自2019年8月1日起正式取消银行开户许可证，简化企业开户流程。银行开户时间缩短至半个工作日。
                </p>
