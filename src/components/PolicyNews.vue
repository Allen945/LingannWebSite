                <h3>国务院关于同意设立中国（上海）自由贸易试验区临港新片区的批复</h3>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;国函〔2019〕68号</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;上海市人民政府、商务部：</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;你们关于增设中国（上海）自由贸易试验区新片区的请示收悉。现批复如下：</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;一、同意设立中国（上海）自由贸易试验区临港新片区。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;二、中国（上海）自由贸易试验区临港新片区先行启动区域面积为119.5平方公里（具体四至范围见附件）。落桩定界工作，经商务部、自然资源部审核验收后报国务院备案，由商务部、自然资源部负责发布。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;三、上海市人民政府、商务部要会同有关部门做好《中国（上海）自由贸易试验区临港新片区总体方案》的组织实施工作。
                    附件：中国（上海）自由贸易试验区临港新片区先行启动区域四至范围
                    国务院
                    2019年7月27日
                    (此件公开发布)
                    附件
                </p>
