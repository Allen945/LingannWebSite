                    区块二10.5平方公里，四至范围：东至沪芦高速、芦潮引河，南至环南一路、环南二路，西至南芦公路，北至胜利塘、顺翔路。
                    区块三18.8平方公里，四至范围：东至H61路、北护城河、S10路、B42路、环湖二路、E7路，南至海塘大堤，西至沪芦高速、芦潮引河、海港大道、环湖西路、B41路、环湖三路、S7路、北护城河、随塘河、海西路，北至三三公路。
                    二、小洋山岛区域共18.3平方公里
                    四至范围：小洋山岛全域。
                    中国（上海）自由贸易试验区
                    临港新片区总体方案
                    设立中国（上海）自由贸易试验区临港新片区（以下简称新片区），是以习近平同志为核心的党中央总揽全局、科学决策作出的进一步扩大开放重大战略部署，是新时代彰显我国坚持全方位开放鲜明态度、主动引领经济全球化健康发展的重要举措。为深入贯彻习近平总书记在首届中国国际进口博览会期间的重要讲话精神，在更深层次、更宽领域、以更大力度推进全方位高水平开放，制定本方案
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;一、总体要求</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（一）指导思想。以习近平新时代中国特色社会主义思想为指导，全面贯彻党的十九大和十九届二中、三中全会精神，坚持新发展理念，坚持高质量发展，推动经济发展质量变革、效率变革、动力变革，对标国际上公认的竞争力最强的自由贸易园区，选择国家战略需要、国际市场需求大、对开放度要求高但其他地区尚不具备实施条件的重点领域，实施具有较强国际市场竞争力的开放政策和制度，加大开放型经济的风险压力测试，实现新片区与境外投资经营便利、货物自由进出、资金流动便利、运输高度开放、人员自由执业、信息快捷联通，打造更具国际市场影响力和竞争力的特殊经济功能区，主动服务和融入国家重大战略，更好服务对外开放总体战略布局。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;（二）发展目标。到2025年，建立比较成熟的投资贸易自由化便利化制度体系，打造一批更高开放度的功能型平台，集聚一批世界一流企业，区域创造力和竞争力显著增强，经济实力和经济总量大幅跃升。到2035年，建成具有较强国际市场影响力和竞争力的特殊经济功能区，形成更加成熟定型的制度成果，打造全球高端资源要素配置的核心功能，成为我国深度融入经济全球化的重要载体。
                    （
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;三）规划范围。在上海大治河以南、金汇港以东以及小洋山岛、浦东国际机场南侧区域设置新片区。按照“整体规划、分步实施”原则，先行启动南汇新城、临港装备产业区、小洋山岛、浦东机场南侧等区域，面积为119.5平方公里。
                    新片区的开发利用须遵守土地、无居民海岛利用和生态环境、城乡规划等法律法规，并符合节约集约利用资源的有关要求；支持按照国家相关法规和程序，办理合理必需用海。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;二、建立以投资贸易自由化为核心的制度体系
                    在适用自由贸易试验区各项开放创新措施的基础上，支持新片区以投资自由、贸易自由、资金自由、运输自由、人员从业自由等为重点，推进投资贸易自由化便利化
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（四）实施公平竞争的投资经营便利。借鉴国际上自由贸易园区的通行做法，实施外商投资安全审查制度，在电信、保险、证券、科研和技术服务、教育、卫生等重点领域加大对外开放力度，放宽注册资本、投资方式等限制，促进各类市场主体公平竞争。探索试行商事主体登记确认制，尊重市场主体民事权利，对申请人提交的文件实行形式审查。深入实施“证照分离”改革。支持新片区加强国际商事纠纷审判组织建设。允许境外知名仲裁及争议解决机构经上海市人民政府司法行政部门登记并报国务院司法行政部门备案，在新片区内设立业务机构，就国际商事、海事、投资等领域发生的民商事争议开展仲裁业务，依法支持和保障中外当事人在仲裁前和仲裁中的财产保全、证据保全、行为保全等临时措施的申请和执行。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（五）实施高标准的贸易自由化。在新片区内设立物理围网区域，建立洋山特殊综合保税区，作为对标国际公认、竞争力最强自由贸易园区的重要载体，在全面实施综合保税区政策的基础上，取消不必要的贸易监管、许可和程序要求，实施更高水平的贸易自由化便利化政策和制度。对境外抵离物理围网区域的货物，探索实施以安全监管为主、体现更高水平贸易自由化便利化的监管模式，提高口岸监管服务效率，增强国际中转集拼枢纽功能。支持新片区发展具有国际竞争力的重点产业，根据企业的业务特点，积极探索相适应的海关监管制度。相关监管政策制度由海关总署牵头另行制定。推进服务贸易自由化，加快文化服务、技术产品、信息通讯、医疗健康等资本技术密集型服务贸易发展，创新跨境电商服务模式，鼓励跨境电商企业在新片区内建立国际配送平台。根据油气体制改革进程和产业需要，研究赋予新片区内符合条件的企业原油进口资质。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（六）实施资金便利收付的跨境金融管理制度。在风险可控的前提下，按照法律法规规定，借鉴国际通行的金融监管规则，进一步简化优质企业跨境人民币业务办理流程，推动跨境金融服务便利化。研究开展自由贸易账户本外币一体化功能试点，探索新片区内资本自由流入流出和自由兑换。支持新片区内企业参照国际通行规则依法合规开展跨境金融活动，支持金融机构在依法合规、风险可控、商业可持续的前提下为新片区内企业和非居民提供跨境发债、跨境投资并购和跨境资金集中运营等跨境金融服务。新片区内企业从境外募集的资金、符合条件的金融机构从境外募集的资金及其提供跨境服务取得的收入，可自主用于新片区内及境外的经营投资活动。支持符合条件的金融机构开展跨境证券投资、跨境保险资产管理等业务。按照国家统筹规划、服务实体、风险可控、分步推进的原则，稳步推进资本项目可兑换。先行先试金融业对外开放措施，积极落实放宽金融机构外资持股比例、拓宽外资金融机构业务经营范围等措施，支持符合条件的境外投资者依法设立各类金融机构，保障中外资金融机构依法平等经营。经国家金融管理部门授权，运用科技手段提升金融服务水平和监管能力，建立统一高效的金融管理体制机制，切实防范金融风险。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（七）实施高度开放的国际运输管理。提升拓展全球枢纽港功能，在沿海捎带、国际船舶登记、国际航权开放等方面加强探索，提高对国际航线、货物资源的集聚和配置能力。逐步放开船舶法定检验。在确保有效监管、风险可控前提下，对境内制造船舶在“中国洋山港”登记从事国际运输的，视同出口，给予出口退税。进一步完善启运港退税相关政策，优化监管流程，扩大中资方便旗船沿海捎带政策实施效果，研究在对等原则下允许外籍国际航行船舶开展以洋山港为国际中转港的外贸集装箱沿海捎带业务。推动浦东国际机场与“一带一路”沿线国家和地区扩大包括第五航权在内的航权安排，吸引相关国家和地区航空公司开辟经停航线。支持浦东国际机场探索航空中转集拼业务。以洋山深水港、浦东国际机场与芦潮港铁路集装箱中心站为载体，推动海运、空运、铁路运输信息共享，提高多式联运的运行效率。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（八）实施自由便利的人员管理。放宽现代服务业高端人才从业限制，在人员出入境、外籍人才永久居留等方面实施更加开放便利的政策措施。建立外国人在新片区内工作许可制度和人才签证制度。允许具有境外职业资格的金融、建筑、规划、设计等领域符合条件的专业人才经备案后，在新片区内提供服务，其在境外的从业经历可视同国内从业经历。除涉及国家主权、安全外，允许境外人士在新片区内申请参加我国相关职业资格考试。探索在法医毒物司法鉴定、环境损害司法鉴定等技术含量高的领域开展和加强技术合作。为到新片区内从事商务、交流、访问等经贸活动的外国人提供更加便利的签证和停居留政策措施。制定和完善海外人才引进政策和管理办法，给予科研创新领军人才及团队等海外高层次人才办理工作许可、永久或长期居留手续“绿色通道”。探索实施外籍人员配额管理制度，为新片区内注册企业急需的外国人才提供更加便利的服务。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（九）实施国际互联网数据跨境安全有序流动。建设完备的国际通信设施，加快5G、IPv6、云计算、物联网、车联网等新一代信息基础设施建设，提升新片区内宽带接入能力、网络服务质量和应用水平，构建安全便利的国际互联网数据专用通道。支持新片区聚焦集成电路、人工智能、生物医药、总部经济等关键领域，试点开展数据跨境流动的安全评估，建立数据保护能力认证、数据流通备份审查、跨境数据流通和交易风险评估等数据安全管理机制。开展国际合作规则试点，加大对专利、版权、企业商业秘密等权利及数据的保护力度，主动参与引领全球数字经济交流合作。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十）实施具有国际竞争力的税收制度和政策。对境外进入物理围网区域内的货物、物理围网区域内企业之间的货物交易和服务实行特殊的税收政策。扩大新片区服务出口增值税政策适用范围，研究适应境外投资和离岸业务发展的新片区税收政策。对新片区内符合条件的从事集成电路、人工智能、生物医药、民用航空等关键领域核心环节生产研发的企业，自设立之日起5年内减按15%的税率征收企业所得税。研究实施境外人才个人所得税税负差额补贴政策。在不导致税基侵蚀和利润转移的前提下，探索试点自由贸易账户的税收政策安排。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;三、建立全面风险管理制度
                    以风险防控为底线，以分类监管、协同监管、智能监管为基础，全面提升风险防范水平和安全监管水平。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十一）强化重点领域监管。建立涵盖新片区管理机构、行业主管部门、区内企业和相关运营主体的一体化信息管理服务平台。聚焦投资、贸易、金融、网络、生态环境、文化安全、人员进出、反恐反分裂、公共道德等重点领域，进一步完善外商投资安全审查、反垄断审查、行业管理、用户认证、行为审计等管理措施，在风险研判和防控中加强信息技术应用，建立联防联控机制，实施严格监管、精准监管、有效监管。建立检疫、原产地、知识产权、国际公约、跨境资金等特殊领域的风险精准监测机制，实现全流程的风险实时监测和动态预警管理。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十二）加强信用分级管理。完善信用评价基本规则和标准，实施经营者适当性管理，按照“守法便利”原则，把信用等级作为企业享受优惠政策和制度便利的重要依据。建立主动披露制度，实施失信名单披露、市场禁入和退出制度。完善商事登记撤销制度，对以欺骗、贿赂等不正当手段取得登记的，登记机关可以依法撤销登记。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;（十三）强化边界安全。高标准建设智能化监管基础设施，实现监管信息互联互认共享。守住“一线”国门安全、“二线”经济社会安全。加强进境安全管理，对新片区进境货物实行“两段准入”监管模式。对禁限管制（核生化导爆、毒品等）、重大疫情、高风险商品安全等重大紧急或放行后难以管控的风险，以及法律、行政法规有明确要求的，依法实施“准许入境”监管。对非高风险商品检验、风险可控的检疫等其他风险可依法实施“合格入市”监管。
                    四、建设具有国际市场竞争力的开放型产业体系。
                    发挥开放型制度体系优势，推动统筹国际业务、跨境金融服务、前沿科技研发、跨境服务贸易等功能集聚，强化开放型经济集聚功能。加快存量企业转型升级，整体提升区域产业能级
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十四）建立以关键核心技术为突破口的前沿产业集群。建设集成电路综合性产业基地，优化进口料件全程保税监管模式，支持跨国公司设立离岸研发和制造中心，推动核心芯片、特色工艺、关键装备和基础材料等重点领域发展。建设人工智能创新及应用示范区，加快应用场景开放力度，推动智能汽车、智能制造、智能机器人等新产业新业态发展。建设民用航空产业集聚区，以大型客机和民用航空发动机为核心，加速集聚基础研究、技术开发、产品研制、试验验证等配套产业，推动总装交付、生产配套、运营维护、文旅服务等航空全产业链发展。建设面向“一带一路”沿线国家和地区的维修和绿色再制造中心，建立绿色认证和评级体系，支持在综合保税区开展数控机床、工程设备等产品入境维修和再制造，提升高端智能再制造产业国际竞争力。</p>
