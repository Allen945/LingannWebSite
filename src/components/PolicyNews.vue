                <h3>中共上海市委、上海市人民政府关于促进中国（上海）自由贸易试验区临港新片区高质量发展实施特殊支持政策的若干意见</h3>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;
                    20190830《中共上海市委、上海市人民政府关于促进中国（上海）自由贸易试验区临港新片区高质量发展实施特殊支持政策的若干意见》
                    《中共上海市委、上海市人民政府关于促进中国（上海）自由贸易试验区临港新片区高质量发展实施特殊支持政策的若干意见》
                    增设中国（上海）自由贸易试验区临港新片区（以下简称新片区），是习近平总书记交给上海的三项新的重大任务之一。新片区是上海推进改革开放和创新发展的重要载体，是上海面向未来发展的重要战略空间，在上海建设具有世界影响力的社会主义现代化国际大都市、推进全方位高水平开放等重大战略任务中，承担着特殊使命。为落实《长江三角洲区域一体化发展规划纲要》和《中国（上海）自由贸易试验区临港新片区总体方案》，现提出促进新片区高质量发展、实施特殊支持政策的如下意见。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;
                    一、明确总体要求
                    以习近平新时代中国特色社会主义思想为指导，深入贯彻习近平总书记考察上海重要讲话精神，按照习近平总书记“要在更深层次、更宽领域、以更大力度推进全方位高水平开放”的重要指示，支持更高层次、更高水平的对外开放，形成开放型经济新动能；服务高标准高质量建设，提升产业核心竞争力；促进多元功能开发，集聚创新创业和生活服务要素；坚持以人为本，提升城市服务品质，着力把新片区打造成更具国际市场影响力和竞争力的特殊经济功能区，建设成开放创新、智慧生态、产城融合、宜业宜居的现代化新城。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;二、赋予新片区更大改革自主权
                    支持新片区对标最高标准、最好水平，大胆闯、大胆试、自主改，营造更具吸引力的营商环境。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;
                    （一）支持新片区先行先试改革举措。原则上，重大改革举措优先在新片区试点，在新片区适用《中共上海市委、上海市人民政府关于支持浦东新区改革开放再出发实现新时代高质量发展的若干意见》中的各项政策。今后我市出台的政策，对企业和人才的支持力度优于本意见相关规定的，新片区按照“政策从优”原则，普遍适用。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;
                    （二）加大向新片区管理机构放权力度。赋予新片区管理机构市级和区级经济管理权限，在经济调节、行政审批等领域，除确需市级行政机关统一协调管理的事项外，原则上授权或委托新片区管理机构依法行使。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;
                    （三）深化营商环境改革。深化实施“一网通办”和单一综合窗口建设，进一步优化流程，提高办事效率。加大事中事后监管改革创新力度，建立以信用监管为支撑的事中分类评估、事后联动奖惩的监管制度，推动联合监管、动态监管。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;
                    三、打造更具吸引力的人才发展环境
                    实行更加积极、更加开放、更加有效的人才政策，为新片区集聚海内外人才提供坚强有力的保障，让各类人才在新片区各展其才、各尽其用，打造创新活力迸发的海内外人才高地。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;
                    （四）优化新片区人才直接落户政策。赋予新片区管理机构人才引进重点机构推荐权、新片区特殊人才直接申报权、国内人才引进直接落户和留学回国人员落户审批权。对新片区内教育、卫生等公益事业单位录用非上海生源应届普通高校毕业生直接落户打分时加3分。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（五）缩短新片区“居转户”年限。对符合一定工作年限并承诺落户后继续在新片区工作2年以上的人才，“居转户”年限由7年缩短为5年。其中，对符合新片区重点产业布局的用人单位的核心人才，“居转户”年限由7年缩短为3年。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（六）实行居住证专项加分政策。对上海市居住证持证人在新片区工作并居住的，可予以专项加分，即每满1年积2分，满3年后开始计入总积分，最高分值为20分。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（七）拓宽技能人才引进通道。在国家职业资格和技能等级认定范围内，聚焦新片区重点产业布局，制定技能人才引进目录。对该目录以外的紧缺技能岗位核心业务骨干，探索经新片区行业代表性企业自主评定和推荐后，纳入引进范围。对获得中华技能大奖、全国技术能手称号、国务院特殊津贴、世界技能大赛奖项等的人员，以及获得省部级高技能人才最高表彰资助的人员，可不受该目录限制，直接引进落户。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（八）加大人才培养培训扶持力度。支持高层次人才申报相关人才计划项目。围绕“高、精、尖、缺”人才，支持在新片区引进国际化专业技术职业培训项目，实施高级研修和急需紧缺人才培训项目。支持新片区建设产教融合示范区，对符合条件的企业优先认定全市产教融合示范型企业，推进校企共建产教融合实训基地、科研成果转化平台。对新片区重点领域的高技能人才培养基地，在资助额度上予以倾斜。率先试点技能等级认定、新技能培训评价，建立工程技术领域的高技能人才与工程技术人才发展贯通机制。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（九）实行更加灵活的用人机制。对紧缺急需、专业性强的公务员职位采用聘任制，实行协议年薪，一职一薪，并进一步探索更大力度的激励措施。对新片区公务员在遴选、交流、学习、培训、表彰等方面予以优先考虑。创新人才激励方式，对新片区内所有事业单位在编在册工作人员，每人每年增加专项补贴，并纳入单位绩效工资总量。对为新片区建设和发展作出重大成绩、突出贡献人员，给予表彰奖励。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十）放宽现代服务业从业限制。允许具有境外职业资格的金融、建筑、规划、设计等领域符合条件的专业人才经备案后，在新片区提供服务，其在境外的从业经历可视同国内从业经历。允许在新片区工作的境外人才参加房地产估价师、注册城乡规划师等专业技术人才职业资格考试。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十一）大力集聚海外青年人才。在国（境）外高水平大学取得本科及以上学历的优秀外籍毕业生，可直接在新片区工作。上海高校在读外籍留学生可在新片区兼职创业。在新片区设立留学人员创业园，对拟在创业园创办企业的外籍留学人员，直接给予工作许可，并视同工作经历。对新片区管理机构推荐的紧缺急需留学类项目，实施“直通车”制度，给予专项资金优先支持。鼓励新片区企业按照有关规定，招收外籍实习生。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十二）进一步提高入外籍留学人员工作生活便利。在新片区工作的入外籍留学人员可直接办理长期(最长有效期10年)海外人才居住证B证，免办工作许可。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十三）加大力度引进高科技人才和技能型人才。对拟长期在新片区工作的高科技领域外籍人才、外国技能型人才和符合新片区产业发展方向的单位聘雇的外籍人才，放宽年龄、学历和工作经历的限制，经许可，一次性给予2年以上的工作许可。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十四）赋予新片区管理机构外籍人才加分权及上海科技创新职业清单推荐权。经新片区管理机构认定的外籍人才,可享受外国人来华工作许可和外国人才签证计点积分鼓励性加分。新片区管理机构可根据重点产业布局，直接推荐新片区内的重点企事业单位进入上海科技创新职业清单。清单内单位聘雇的管理或技术职务的外籍人员，可享受相关便利。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十五）建立境外人才工作和创业绿色通道。在新片区试点实行外国人来华工作许可差异化流程，对新片区管理机构认定的信用企业实行“告知承诺”“容缺受理”等制度。对拥有重大创新技术的外籍高层次人才以技术入股方式在新片区注册企业的，进一步简化办理程序和申请材料。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;四、加大财税金融政策支持力度
                    建立有利于产业和人才集聚的财税制度，按照“地方财力留用、市区专项扶持”的原则，加强财政资金支持。进一步拓展投融资渠道，发挥市场主体建设新片区的作用，为新片区发展提供有力支撑。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十六）建立新片区专项发展资金。新片区产生的所有地方收入，全部用于新片区建设。整合市区两级税收、土地出让收入、基础设施配套费及市区两级专项资金，建立新片区专项发展资金，5年总计出资不少于1000亿元，统筹用于新片区内各类产业扶持、创新创业支持、人才引进培养、基础设施和公共设施建设等。通过专项发展资金，加大对研发创新、技术改造、新产品应用等的支持力度，对重点产业领域核心创新团队给予奖励。对特别重大的项目，新片区管理机构可采用“一事一议”的方式，申请各专项资金支持。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十七）实施税收支持。对新片区内符合条件从事集成电路、人工智能、生物医药、民用航空等关键领域核心环节生产研发的企业，自设立之日起5年内减按15%税率征收企业所得税。对符合条件的集成电路生产、设计和软件企业，按照国家规定，予以享受“两免三减半”“五免五减半”等企业所得税优惠政策。对在新片区工作的境外高端、紧缺人才个人所得税税负差额部分给予补贴。对境外进入物理围网区域内的货物、物理围网区域内企业之间的货物交易和服务，实行特殊的税收政策。扩大新片区服务出口增值税政策适用范围，研究适应境外投资和离岸业务发展的新片区税收政策。在不导致税基侵蚀和利润转移的前提下，探索试点自由贸易账户的税收政策安排。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十八）加大对新片区政府债券发行的支持力度。加大地方政府债券倾斜力度，优先支持新片区符合条件的重大项目。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（十九）对标国际标准，开展跨境金融业务。支持金融机构在依法合规、风险可控、商业可持续的前提下，参照国际通行规则，为新片区内企业和非居民提供跨境发债、跨境投资并购和跨境资金集中运营等跨境金融服务。支持新片区内企业开展真实、合法的离岸转手买卖业务，金融机构可按照国际惯例，为新片区内企业开展离岸转手买卖业务提供高效便利的跨境金融服务。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（二十）加强跨境资金灵活使用。新片区内企业从境外募集的资金、符合条件的金融机构从境外募集的资金及其提供跨境服务取得的收入，可自主用于新片区及境外的经营投资活动。对新片区内符合条件的诚信优质企业，经新片区管理机构认定，可试点开展外汇收支便利化。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（二十一）推进建设资金管理中心。适当降低开展跨境资金集中运营业务的准入门槛，进一步便利新片区内企业开展跨境资金双向归集，实现资金集中管理。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（二十二）发挥银行信贷、保险资金以及融资担保基金等作用。研究出台对重点产业长期低息贷款并吸引保险资本支持政策，以及市、区两级配套贴息政策。鼓励创新融资担保支持方式，加大市政策性融资担保基金对新片区内中小企业的支持力度。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（二十三）提高重点产业直接融资规模。优先支持符合条件的集成电路、人工智能、生物医药、航空航天、新能源汽车等关键重点领域的企业上市。支持新片区设立战略性新兴产业投资平台，创新股权投资等方式，带动社会资本投向重大产业项目、初创型企业等。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;五、加大规划土地政策支持力度
                    以规划为引领，优化新片区空间格局，提高经济密度，促进资源要素高效率配置。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（二十四）编制新片区国土空间规划。新片区国土空间规划在浦东新区等区级总体规划中单独成章，并做好与市、区两级总体规划的衔接。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（二十五）进一步完善资源性要素配置的市级统筹机制。对新片区新增建设用地指标实行市级单列，与减量化指标脱钩。重大项目能耗和污染物排放指标由市级统筹。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（二十六）提高新片区工业、研发用地容积率。对符合新片区产业功能的项目，容积率可进一步提高。对存量工业、研发用地提高容积率的，根据持有比例，经新片区管理机构决策，可减免增容土地出让价款。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（二十七）鼓励土地节约集约混合利用。探索实行混合用地、创新型产业用地等政策，推进工业、研发办公、中试生产等功能混合，引导科技研发、企业总部管理等创新功能加快集聚。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（二十八）支持新片区内园区平台提升创新服务能力。新片区内园区平台收购产业项目类存量工业用地用于通用类研发平台和标准厂房的，在明确土地利用绩效和退出机制等全生命周期管理基础上，可将不超过物业总量的50%转让给研发机构或企业，其中直接转让比例不超过物业总量的30%，鼓励先租后让。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp; 六、推动高端产业集聚发展
                    坚持创新引领，着力打造世界级前沿产业集群，提升科技创新和产业融合能力，加快形成新动能，增强国际竞争新优势。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（二十九）推动重大项目和平台向新片区集聚。支持集成电路、人工智能、生物医药、航空航天、新能源和智能网联汽车、智能制造、高端装备等领域重点项目优先在新片区布局。在新片区布局建设工程研究中心、重点实验室等关键功能性支撑平台。引导相关领域的重点科研院所、研发机构、标准制定企业、检验检测机构、计量测试平台等向新片区集聚。支持企业联合研究机构和行业上下游共建产业协同创新共同体，建设产业创新中心。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（三十）加快布局新一代信息基础设施。制定新片区通信网络规划，率先布局双千兆网络建设，支持基础电信运营商在新片区共建、共用、共享5G基础设施，支持基础电信运营商、互联网以及各行业龙头企业在新片区内协同开展双千兆网络应用示范区建设。深入推进IPv6规模部署，统筹规划新片区互联网数据中心及边缘数据中心布局，加快云计算、物联网、新型互联网交换中心等信息基础设施建设。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（三十一）建设工业互联网。建设“工业互联网+人工智能”创新和应用示范区，发挥好国家互联网标识解析国家顶级节点的作用，引导新片区内重点行业和重要企业建设工业互联网标识解析二级节点和标识解析企业节点，推动标识解析在重点领域的运用。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（三十二）推进智能应用。围绕数字制造、智慧交通、智能治理，推动应用场景开放。探索全面放开智能网联汽车道路测试，布局车路协同设施建设。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（三十三）授予新片区管理机构高新技术企业认定权。新片区管理机构按照统一标准，对新片区内企业进行国家高新技术企业认定和市级高新技术企业培育入库认定，落实高新技术企业所得税优惠、研发费用加计扣除等政策。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（三十四）压缩专利审查时限。支持在中国（浦东）知识产权保护中心设立新片区专窗，加快生物医药、集成电路、脑科学与人工智能等领域专利审查速度。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;七、加大对人才的住房保障力度
                    坚持职住平衡，针对多层次住房需求，建立以市场供应为主、多主体供应、多渠道保障、租购并举的区域性住房体系。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（三十五）定向微调新片区住房限购政策。按照区域发展和产业导向，对符合一定条件的非本市户籍人才，购房资格由居民家庭调整为个人，可购买新片区普通商品房一套。缩短非本市户籍人才在新片区购房缴纳个人所得税或社会保险金的年限，将自购房之日前连续缴纳满5年及以上，调整为连续缴纳满3年及以上。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;（三十六）调整商品住房选房购房制度。符合购房条件、在新片区稳定就业且稳定居住的常住人口，可在新片区优先选房购房。
                    （三十七）实施限价商品房政策。控制限价商品房供应量，加大人才公寓供给力度，将部分限价商品房在房源性质不变的情况下，转为人才公寓使用；聚焦激励人才，提高供应的精准性，对新片区有贡献的企业和人才予以倾斜。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（三十八）建设“先租后售”公租房。建成后10年内作为公租房使用，其中50%房源可由单位按门栋整体购买作为公租房中单位租赁房使用。公租房建成10年后可作为商品住房按套上市转让。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（三十九）扩大租赁住房配套建设渠道。商业办公用地配套建设租赁住房等生活服务设施的，其建筑面积占项目总建筑面积的比例上限从10%提高至15%。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（四十）给予规划土地政策支持。对新片区新建人才公寓、租赁住房在供地方式、供地价格上予以规划土地政策支持。支持对租赁住房的建设、筹措，扩大集体土地建设租赁住房试点。支持非房地产企业依法取得土地用于建设租赁住房。探索开展租赁住房用地出让价款分期收取试点。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;八、构建便捷的交通网络体系
                    加强基础设施建设，提高区域连接能力，着力构建对外高效畅达、对内便捷绿色、管理智能便民的综合交通体系，将新片区建设成为长三角区域重要节点城市。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;（四十一）制定新片区综合交通规划。完善区域综合交通网络，优化内外交通衔接，有序安排实施计划，提升综合交通效率。
                    重点加强新片区与浦东国际机场、中心城区以及长三角重点城市之间的综合交通网络联系。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（四十二）推进铁路和轨道交通网络建设。加强浦东综合交通枢纽以及与长江北部和浙北地区的铁路通道建设，实现与长三角地区铁路运输快速通行。完善新片区内轨道交通网络，加快提升轨道交通运能。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（四十三）加强新片区与周边区域高速公路建设。提升新片区与浦东国际机场、中心城区及其他周边区域之间高速公路快速通行能力。疏解洋山港货物公路运输交通压力，推进洋山港至G1503公路段客货分流改造。继续减免S2高速公路客车通行收费。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（四十四）提升新片区内客运能力。支持新片区规划建设中运量局域线，加快线网规划，启动线网建设。增强新片区内轨道交通重要站点与主要集中居住区、重点功能区、重大赛事场馆以及旅游活动场馆之间短驳公交配置。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（四十五）强化新片区多式联运集疏运体系。依托洋山深水港、浦东国际机场、芦潮港铁路集装箱中心站、南港码头等，进一步增强海铁联运、江海联运、水水中转功能。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;九、提升城市综合服务功能
                    坚持产城融合，不断丰富城市服务功能，打造高品质生活，建设宜业宜居现代化国际新城。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（四十六）丰富提升教育资源。深化推进新片区高水平高校发展，支持新片区内具备条件的大学加快建设一流学科，支持高校开展地方高水平高校及高水平应用型高校建设。推动义务教育阶段学区化、集团化办学全覆盖，促进优秀教师跨校流动。支持我市优质教育集团在新片区开办分校或合作办学。支持引进高水平国际学校。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（四十七）进一步优化配置医疗资源。支持境外医疗机构在新片区办医，支持市级医疗机构在新片区发展特色专科，做实家庭医生签约服务，实现基于居民电子健康档案的动态健康管理。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（四十八）加快文化休闲、商业网点设施建设。加快推进青少年活动中心等艺术、体育、文化重点城市功能项目建设。建设临港帆船帆板基地，打造高品质水上运动中心。建设符合新片区特点、具有国际特色的体育休闲旅游集聚区。对连锁商业实行“一照多址”“一证多址”的准入制度。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;（四十九）打造绿色生态环境。加强河流、湖泊、林地、野生动物栖息地等生态区域保护，推进滨江沿海岸线绿道和郊野公园、城市公园等建设，促进生态空间与市民游憩空间结合。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;（五十）加强区域精细化管理。以全覆盖、法治化、智能化、标准化为着力点，运用信息技术手段，提升社会治理智能化水平，建立高标准的新片区综合管理标准体系，加强环境综合治理，加强基层建设，努力提高新片区区域治理整体能力，为新片区人民群众提供精细化的管理服务。
                    本意见适用于新片区管理机构管辖范围。《中国（上海）自由贸易试验区临港新片区总体方案》有明确规定的，按照相关规定执行。
                    本意见从2019年9月1日起实施，有效期至2023年8月31号。
                </p>
