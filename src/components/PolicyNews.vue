                <h3>0190820中国（上海）自由贸易试验区临港新片区管理办法</h3>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;20190820中国（上海）自由贸易试验区临港新片区管理办法
                    上海市人民政府网站今天全文发布《中国（上海）自由贸易试验区临港新片区管理办法》，该管理办法自2019年8月20日起施行。
                    中国（上海）自由贸易试验区临港新片区管理办法
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;第一章总则</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第一条（目的和依据）
                    为了推进中国（上海）自由贸易试验区临港新片区建设，在更深层次、更宽领域、以更大力度推进全方位高水平开放，打造更具国际市场影响力和竞争力的特殊经济功能区，根据《中国（上海）自由贸易试验区临港新片区总体方案》和有关法律、法规，制定本办法。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第三条（区域功能）
                    新片区对标国际上公认的竞争力最强的自由贸易园区，在适用自由贸易试验区各项开放创新措施的基础上，实施具有较强国际市场竞争力的开放政策和制度，加大开放型经济的风险压力测试，实现新片区与境外投资经营便利、货物自由进出、资金流动便利、运输高度开放、人员自由执业、信息快捷联通。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第四条（产城融合）
                    新片区鼓励国际优质资本进入教育、医疗、养老、文化、体育、园区建设、城市运行等公共服务领域，加强各类基础设施建设管理，提升高品质国际化的城市服务功能，打造开放创新、智慧生态、产城融合、宜业宜居的现代化新城。
                    第二章管理体制
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第五条（管理机构）
                    中国（上海）自由贸易试验区临港新片区管理委员会（以下简称管委会）作为市人民政府的派出机构，负责具体落实新片区各项改革试点任务，承担新片区经济管理职责，统筹管理和协调新片区有关行政事务。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第六条（机构职责）
                    管委会依照本办法履行以下职责：
                    （一）组织实施新片区发展规划和政策措施，制定有关行政管理制度；
                    （二）负责投资、贸易、金融服务、规划资源等有关行政管理工作；
                    （三）领导或者协调驻区机构有关行政管理工作，与驻区机构、有关部门建立合作协调和联动执法工作机制；
                    （四）统筹推进新片区开发建设，统筹指导新片区内产业布局和功能培育，协调推进重大投资项目建设；
                    （五）落实“一网通办”要求，全面实行“互联网+政务服务”，为新片区内企业和相关机构提供指导、咨询和服务；
                    （六）领导、管理南汇新城镇，统筹新片区其他镇的经济管理事务，协同相关区做好新片区内社会事务的协调和管理；
                    （七）履行市人民政府赋予的其他职责。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第七条（行政审批和行政处罚）
                    新片区实行综合审批、相对集中行政处罚的体制和机制，由管委会集中行使本市有关行政审批权和行政处罚权。管委会实施行政审批和行政处罚的具体事项，由市人民政府另行规定。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第八条（自主改革创新）
                    管委会应当充分运用国家赋予新片区的自主发展、自主改革、自主创新管理权限，在风险可控的前提下自主开展适应市场需求的创新业务。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第九条（其他行政事务）
                    市有关部门和相关区人民政府应当加强协作，支持新片区改革创新和管委会的各项工作，并按照各自职责，承担管委会职责范围之外的其他行政事务。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第十条（试点赋权机构）
                    在新片区试点设立依法授权的专业机构，承担公共服务、决策咨询、风险评估等事务，提高新片区公共服务水平和行政决策质量。
                    第三章投资经营便利
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第十一条（重点领域对外开放）
                    新片区建立开放型制度体系，在电信、保险、证券、科研和技术服务、教育、卫生等重点领域加大对外开放力度，放宽注册资本、投资方式等限制，促进各类市场主体公平竞争，推动集成电路、人工智能、生物医药、航空航天、新能源汽车、高端智能装备等产业集聚。
                    新片区根据先行先试推进情况和发展需要，不断扩大开放的领域、试点内容以及相应的制度创新措施。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第十二条（商事主体登记确认制）
                    新片区试行商事主体登记确认制。登记机关对申请人提交的登记材料实行形式审查，审查通过后即时予以登记确认，核发营业执照。
                    申请人应当履行真实、完整和准确申报的义务。企业名称应当遵守国家有关规定，不得损害国家、社会公共利益，不得违背公序良俗或者有其他不良影响。
                    对提供虚假材料、作出虚假承诺以及通过欺骗、贿赂等不正当手段取得登记的，登记机关应当依法撤销登记。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第十三条（民商事争议解决）
                    境外知名仲裁及争议解决机构经市司法行政部门登记并报国务院司法行政部门备案后，可以在新片区内设立业务机构，就国际商事、海事、投资等领域发生的民商事争议开展仲裁业务。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第四章贸易自由
                    第十四条（洋山特殊综合保税区）
                    在新片区内设立物理围网区域，依法建立洋山特殊综合保税区，在全面实施综合保税区政策的基础上，优化贸易监管、许可和程序要求，实施更高水平的贸易自由化便利化政策和制度。
                    对境外抵离物理围网区域的货物，实施以安全监管为主、更高水平的贸易自由化便利化监管模式。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第十五条（新型国际贸易）
                    新片区完善新型国际贸易与国际市场投融资服务的系统性制度支撑体系，吸引总部型机构集聚。
                    新片区建设国际医疗服务集聚区，支持与境外机构合作开发跨境医疗保险产品，开展国际医疗保险结算试点。
                    新片区内符合条件的外商独资企业可以开展面向全球的文化艺术品展示、拍卖和交易。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第十六条（促进跨境电商发展）
                    新片区发展跨境数字贸易，创新跨境电商服务模式，鼓励跨境电商企业在新片区内设立国际配送平台。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第十七条（促进服务贸易）
                    新片区加快推动文化服务、技术产品、信息通讯、医疗健康等资本技术密集型服务贸易发展，推进服务贸易自由化。
                    第五章金融开放
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第十八条（金融业对外开放）
                    在新片区内先行先试金融业对外开放措施，落实放宽金融机构外资持股比例、拓宽外资金融机构业务经营范围等措施，支持符合条件的境外投资者依法设立各类金融机构，保障中外资金融机构依法平等经营。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第十九条（跨境金融服务）
                    支持新片区内企业参照国际通行规则依法合规开展跨境金融活动，支持金融机构在依法合规、风险可控、商业可持续的前提下，为新片区内企业和非居民提供跨境发债、跨境投资并购和跨境资金集中运营等跨境金融服务。
                    支持新片区内企业开展真实、合法的离岸转手买卖业务。金融机构可以按照国际惯例，为新片区内企业开展离岸转手买卖业务提供高效便利的跨境金融服务。
                    新片区支持符合条件的金融机构开展跨境证券投资、跨境保险资产管理等业务。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第二十条（人民币跨境业务）
                    在风险可控的前提下，根据法律法规规定，借鉴国际通行的金融监管规则，进一步简化优质企业跨境人民币业务办理流程，实施更加便利的跨境金融服务措施。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第二十一条（境外资金使用）
                    新片区内企业从境外募集的资金，可以自主用于新片区内以及境外的经营投资活动。
                    符合条件的金融机构从境外募集的资金及其提供跨境服务取得的收入，可以自主用于新片区内以及境外的经营投资活动。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第二十二条（资本项目开放）
                    推动自由贸易账户本外币一体化功能试点，探索新片区内资本自由流入流出和自由兑换。
                    新片区按照国家统筹规划、服务实体、风险可控、分步推进的原则，稳步推进资本项目可兑换。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第二十三条（资金管理中心）
                    新片区鼓励跨国公司设立全球或者区域资金管理中心，实施更加便利的跨境资金双向归集管理模式。
                    第六章国际运输便利
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第二十四条（国际船舶登记）
                    新片区实行更加便利的“中国洋山港”籍船舶登记管理制度，逐步放开船舶法定检验。
                    在确保有效监管、风险可控前提下，对境内制造船舶在“中国洋山港”登记从事国际运输的，视同出口，按照国家规定给予出口退税。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第二十五条（国际航运服务）
                    新片区支持内外资企业和相关机构开展航运融资、航运保险、航运结算、航材租赁、船舶交易和航运仲裁等服务；建设国际航运补给服务体系；推动发展航运指数衍生品业务。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第二十六条（启运港退税）
                    进一步完善启运港退税相关政策，对符合条件的出口企业经洋山港离境的集装箱货物，实行更加便利高效的监管和服务。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第二十七条（多式联运）
                    新片区以洋山深水港、浦东国际机场和芦潮港铁路集装箱中心站为载体，推动海运、空运、铁路运输信息共享，提高多式联运的运行效率。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第二十八条（国际航空业务）
                    支持浦东国际机场开展航空中转集拼业务，实行更加便利的海关监管制度。
                    支持浦东国际机场建设具有物流、分拣和监管集成功能的航空货站，实行更加便利的航空货运监管模式和货机机组人员出入境边防检查管理模式。
                    对国际中转旅客及其行李，实行通程联运，进一步缩短中转衔接时间。
                    第七章人才服务
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第二十九条（优化人才政策）
                    新片区加大人才引进培养扶持力度，为集聚海内外人才在新片区各展其才、各尽其用提供保障。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第三十条（户籍和居住证）
                    优化新片区人才直接落户政策，对符合条件的人才，缩短新片区居住证转办常住户口年限。对持有本市居住证在新片区工作并居住的人员，实行居住证专项加分。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第三十一条（技能人才引进）
                    在国家职业资格和技能等级认定范围内，聚焦新片区重点产业布局，制定技能人才引进目录。
                    对目录以外的紧缺技能岗位核心业务骨干，由新片区行业代表性企业自主评定和推荐后纳入引进范围。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第三十二条（从业资格）
                    允许具有境外职业资格的金融、建筑、规划、设计等领域符合条件的专业人才经备案后，在新片区内提供服务，其境外从业经历可以视同国内从业经历。
                    除涉及国家主权和安全外，允许境外人员在新片区申请参加我国相关职业资格考试。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第三十三条（出入境和停居留便利）
                    对到新片区从事商务、交流、访问等经贸活动的外国人，给予签证和停居留便利。
                    新片区放宽现代服务业高端人才从业限制，在人员出入境、外籍人才永久居留等方面实施更加开放便利的政策措施。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第三十四条（外籍人员工作便利）
                    对拟长期在新片区工作的高科技领域外籍人才、外国技能型人才，以及符合新片区产业发展方向的单位聘用的外籍人才，放宽年龄、学历和工作经历限制，按照相关规定一次性给予2年以上的外国人来华工作许可。
                    已加入外籍的留学人员在新片区工作的，可以直接办理长期海外人才居住证，免于办理外国人来华工作许可。取得境外高水平大学本科及以上学历的优秀外籍毕业生，可以直接在新片区工作。
                    已加入外籍的留学人员和取得境外高水平大学本科及以上学历的优秀外籍毕业生，在新片区留学人员创业园创办企业的，直接给予外国人来华工作许可，其创业经历可以视同工作经历。
                    第八章信息快捷联通
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第三十五条（互联网基础设施）
                    新片区建设完备的国际通信设施，加快新一代信息基础设施建设，提升宽带接入能力、网络服务质量和应用水平,构建安全便利的国际互联网数据专用通道。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第三十六条（跨境数据流动）
                    新片区聚焦集成电路、人工智能、生物医药和总部经济等关键领域，试点开展数据跨境流动的安全评估，建立数据保护能力认证、数据流通备份审查、跨境数据流通和交易风险评估等数据安全管理机制。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第三十七条（知识产权和数据保护）
                    新片区开展国际合作规则试点,加大对专利、版权、商业秘密等权利和数据的保护力度，主动参与引领全球数字经济交流合作。
                    第九章财税支持
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第三十八条（国际业务税收政策）
                    扩大新片区服务出口增值税政策适用范围，研究适应境外投资和离岸业务发展的新片区税收政策。在不导致税基侵蚀和利润转移的前提下，探索试点自由贸易账户的税收政策安排。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第三十九条（重点产业和人才税收支持）
                    对新片区内符合条件的从事集成电路、人工智能、生物医药、民用航空等关键领域核心环节生产研发的企业，按照相关规定给予企业所得税支持。研究实施境外人才个人所得税支持政策。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第四十条（专项资金扶持）
                    本市设立新片区专项发展资金，统筹用于新片区产业扶持、创新创业支持、人才引进培养、基础设施和公共设施建设等方面。
                    新片区通过设立战略性新兴产业投资平台、创新股权投资等方式，带动社会资本投向重大产业项目、初创型企业等领域。
                    第十章风险防范
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第四十一条（全面风险管理）
                    新片区以风险防控为底线，以分类监管、协同监管、智能监管为基础，全面提升风险防范水平和安全监管水平。
                    在新片区创新行政管理方式，推动形成市场主体自律、业界自治、社会监督、政府监管的综合监管体系。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第四十二条（重点领域监管）
                    新片区聚焦投资、贸易、金融、网络、生态环境、文化安全、人员进出、反恐反分裂、公共道德等重点领域，进一步完善外商投资安全审查、反垄断审查、行业管理、用户认证、行为审计等管理措施，实施严格监管、精准监管和有效监管。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第四十三条（信息管理服务平台）
                    新片区建立涵盖管委会、行业主管部门、区内企业和相关运营主体的一体化信息管理服务平台，在风险研判和防控中加强信息技术应用。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第四十四条（金融风险防范）
                    经国家金融管理部门授权，建立统一高效的金融管理体制机制，加强与国家金融管理部门协作。运用科技手段提升金融服务水平和监管能力，依法打击各类非法金融活动，切实防范金融风险。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第四十五条（监测和预警）
                    新片区建立检疫、原产地、知识产权、国际公约、跨境资金等特殊领域风险精准监测机制，实现全流程风险实时监测和动态预警管理。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第四十六条（信用分级管理）
                    新片区实施经营者适当性管理。完善信用评价基本规则和标准，按照守信便利原则，把信用等级作为企业享受优惠政策和制度便利的重要依据。
                    新片区建立主动披露制度，实施失信名单披露、市场禁入和退出制度。
                </p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第四十七条（边界安全监管）
                    新片区建设高标准智能化监管基础设施,实现监管信息互联互认共享，加强进出境安全管理，优化新片区进出境货物监管模式。
                </p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;第十一章附则</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;第四十八条（政策适用）
                    自由贸易试验区各项开放创新措施适用于新片区，本办法作出规定的事项按照本办法执行。
                    本办法施行后，本市出台的相关政策措施，有利于促进新片区发展的，新片区可以直接适用。
                </p>
